function getTranslation(key) {
    const lang = localStorage.getItem('selectedLang') || 'en';
    return (translations[lang] && translations[lang][key]) || translations['en'][key] || key;
}

function setLanguage(lang) {
    localStorage.setItem('selectedLang', lang);
    applyTranslations(lang);
    
    // Update all language select dropdowns to match
    document.querySelectorAll('.language-selector').forEach(select => {
        select.value = lang;
    });
}

function applyTranslations(lang) {
    const dict = translations[lang] || translations['en'];
    
    // 1. Text elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = dict[key];
            } else {
                el.innerText = dict[key];
            }
        }
    });

    // 2. CSS-based pseudo elements using data-label
    document.querySelectorAll('[data-label-i18n]').forEach(el => {
        const key = el.getAttribute('data-label-i18n');
        if (dict[key]) {
            el.setAttribute('data-label', dict[key]);
        }
    });

    // 3. Document Title
    if (dict['brand_name']) {
        const baseTitle = document.title.split('|')[0].trim();
        document.title = `${baseTitle} | ${dict['brand_name']}`;
    }

    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLang') || 'en';
    applyTranslations(savedLang);
    
    document.querySelectorAll('.language-selector').forEach(select => {
        select.value = savedLang;
    });
});
